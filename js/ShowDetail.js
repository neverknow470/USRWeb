var url = location.href;
var id = "";
//再來用去尋找網址列中是否有資料傳遞(QueryString)
if (url.indexOf('?') != -1) {

    //在此直接將各自的參數資料切割放進ary中
    var ary = url.split('?')[1].split('&');
    //下迴圈去搜尋每個資料參數
    for (i = 0; i <= ary.length - 1; i++) {
        //如果資料名稱為id的話那就把他取出來
        if (ary[i].split('=')[0] = 'id')
            id = ary[i].split('=')[1];
    }
}
